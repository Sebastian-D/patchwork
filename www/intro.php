<h5>
Welcome to the homepage and guide for patchwork
</h5>
</div><br />

Patchwork is a bioinformatic tool for analyzing and visualizing allele-specific copy numbers and loss-of-heterozygosity in cancer genomes.
The data input is in the format of whole-genome sequencing data which enables characterization of genomic alterations ranging in size from point mutations to entire chromosomes.<br />
High quality results are obtained even if samples have low coverage, ~4x, low tumor cell content or are aneuploid. <br /><br />


Patchwork is available in two formats. The first, named simply patchwork,
takes BAM files as input whereas patchworkCG takes input from CompleteGenomics files. Detailed guides and information regarding these can be
found in their respective tabs.
<br /><br />

