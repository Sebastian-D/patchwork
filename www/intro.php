Patchwork is a bioinformatic tool for analyzing and visualizing allele-specific copy numbers and loss-of-heterozygosity in cancer genomes.
The data input is in the format of whole-genome sequencing data which enables characterization of genomic alterations ranging in size from point mutations to entire chromosomes.
High quality results are obtained even if samples have low coverage, ~4x, low tumor cell content or are aneuploid.
<br /><br />


Patchwork is available for two data types. Patchwork takes BAM files as input whereas PatchworkCG
takes input from CompleteGenomics files. 
<br /><br />

TAPS performs the same analysis as Patchwork but for microarray data.
<br /><br />

Detailed guides and information regarding all three of these bioinformatic tools can be
found in their respective tabs.
<br /><br />

